/**
 * reset_to_98 - function that takes a pointer to an int as a parameter and
 * updates value it points to 98
 *
 * @n: parameter n is declared as a pointer of int
 * type which is to be passed to function
 *
 * Return: void which means no return type is required
 *
 */

void reset_to_98(int *n)
{
	*n = 98;
}
